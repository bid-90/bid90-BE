package com.bid90.app.DTO;

public class ContactDTO {
	Long id;
	String type;
	String value;
	
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getValue() {
		return value;
	}
	public void setValue(String value) {
		this.value = value;
	}
	public ContactDTO() {
		super();
	}
	
}
